            該死的天滅！
            
            這是搗亂！
            
            這一刻，哪怕神皇也沒多說什么了，喝道：“天古，動用議員令！”
            
            話落，神界當中，飆射出5塊血色令牌！
            
            都是強悍無比，氣息環繞虛空。
            
            這些令牌飛出，瞬間將那些血云壓制了下去，但是還不夠，還沒消失！
            
