            無聲！
            
            這一刻，萬族不少無敵都是駭然。
            
            永恒九段！
            
            差一步，即將合道的強者。
            
            天滅！
            
            都沒幾個人在意的天滅，因為這些鎮守，真的太久太久沒出手了，最近，也只是星宏在出手，云霄也出手了幾次，但是，實力都不夠強大，起碼沒到大秦王那個地步。
            
            而天滅，一棒子打爆了白發的現在身，明顯，已經達到了大秦王那個地步了！
            
            強悍無比！
            
            一尊尊無敵，都是駭然，這家伙，真正的頂級永恒，那36尊石雕，除了天滅，還有這樣的頂級存在嗎？
            
            ……
            
            古城中。
            
            天河死氣消失，也是震驚無比地看向天滅。
            
            我去！
            
            臥槽！
            
            大腿啊，超級大腿，僅次于半皇的大腿啊。
            
            這么牛！
            
            堪比大秦王的存在，這家伙……居然這么強！
            
            萬萬沒想到啊！
            
