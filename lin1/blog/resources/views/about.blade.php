            肉就在眼前，不能吃。
            
            鴻蒙老龜都不許它吃！
            
            老龜沒理它，再次喝道：“還有沒有了？沒有，只能強行破碎！強行破碎，規則反噬，總要有人來承擔！”
            
            他眼神綠油油的，看向神界，看向仙界。
            
            此刻，天古凝眉，看了一眼虛空，平靜道：“規則之力已被壓制大半，剩下的云霄可以承擔！讓她渡劫，渡完了，規則自散！”
            
            老龜還沒說話，天滅就罵道：“不渡！膽大包天，膽敢襲殺吾等鎮守！天古，你來承擔反噬，膽敢說個不字，今日，就破了你仙界！你父昔年見了本座，也要喊一聲天滅大人，你膽子不小！”
            
            天古淡漠道：“天滅鎮守，此刻雷劫已經衰落，云霄渡劫，必然無恙，非要如此咄咄逼人嗎？”
            
            云霄不渡劫，規則不散。
            
            44枚令牌，只能壓制，做不到驅散，唯獨令牌過半，才能讓規則散去。
            
            現在，萬界可能還有一些議員令，可小族若是有，不敢拿出來，強族，也未必全部拿出來了。
            
            一些上古種族，已經覆滅，消失在歷史長河之中。
            
            湊齊99枚令牌，幾乎沒戲。
            
            50枚，也許有點可能。
            
            可是，現在到了這地步，沒有50枚，云霄其實也可以渡劫了，80%的規則之力，都被壓制了！
            
            可天滅不干！
            
            天滅巴不得再出城干他一次，嘿嘿冷笑道：“不渡！你們有能耐就不管不問，誰惹下的禍端，誰來承擔！仙神不承擔，那萬族跟著一起受難！本座也想看看，萬族愿不愿意？魔族，龍族，冥族，靈族，你們樂意嗎？”
            
            一群強者并未開口。
            
            樂意嗎？
            
            廢話！
            
            當然不樂意！
            
            仙神聯手襲擊古城，導致云霄暴動，導致天滅暴動，而今，這樣的麻煩，需要他們出手，已經是很不滿了，還要讓他們承擔反噬，鬼才樂意！
            
            此刻，虛空顫動，一尊尊強悍無邊的身影浮現，有古族強者，暴戾道：“天古，你來承擔反噬！不要再廢話！本王不管你們爭什么，我界萬載以來，不曾參戰，膽敢將我界席卷進入戰爭，破你仙界！”
            
            “寂無，你和天古一起！”
            
            又有一尊巨大無比，高可通天的巨人出現，那是太古巨人皇！
            
            下一刻，虛空中，一頭巨大無比的食鐵獸，隨手抓了一個不知是仙族還是神族的家伙，一口咬了一半，嘎吱嘎吱吃著，慢悠悠道：“快點，打完了睡覺了！天古、寂無……不要再搞事！”
            
            “吼吼吼……”
            
            這一刻，一尊比蘇宇上次見到的更強大的犼出現了，“天古，聽說你仙族的肉最香，給我吃幾個仙王，我幫你解決，反噬我來，反正我不怕！”
            
            “……”
            
            這巨大的上古巨犼，笑聲兇戾，“天古，上古時代，我就想吃你，你爹不答應，現在把你的子孫給我幾個，以后咱們好說話！”
            
            “……”
            
