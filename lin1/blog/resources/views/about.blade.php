            此刻，一尊尊石雕，懸浮上空，虛影凝實。
            
            天滅大吼道：“老大，廢話什么，干他們！艸！合道了不起嗎？星宏、云霄，我們三，殺一個合道玩玩！”
            
            艸！
            
            老子早就不耐煩了，這一刻，他直接本尊騰空，下方，天河瞬間被死氣覆蓋，暗罵一聲。
            
            剛想說話，就見天滅開始朝古城拔去！
            
            天滅古城，漸漸化為一根巨大的鐵棒，這一刻，一條通道，若隱若現間要呈現。
            
            而虛空中，血云瞬間強大三成！
            
            天滅這瘋子，老龜也許是嚇唬人……他不是，他真要拔城而戰了！
            
            天滅哈哈大笑，“老子要出城了，你們等著，殺他個天翻地覆，艸，老子要出來了，要出來了！”
            
            哈哈哈！
            
            他瘋狂大笑，那巨大的古城，已經開始拔地而起。
            
